namespace Airbnb.Domain.Enums;

///<summary>
/// Enumeration representing different types of user roles.
///</summary>
public enum RoleType
{
    ///<summary>
    /// Represents a system role type.
    ///</summary>
    System = 0,

    ///<summary>
    /// Represents an admin role type.
    ///</summary>
    Admin = 1,

    ///<summary>
    /// Represents a user role type.
    ///</summary>
    User = 2
}
